# e imprima a área de um quadrado

print('Com este algoritmo vamos calcular a área de um quadrado')
print('')

lado1 = input('Digite o valor de um dos lados ')
print('--------------------------------------')

lado2 = input('Digite o valor de outro lado ')
print('--------------------------------------')

x = int(lado1)
y = int(lado2)

area = (x * y)

print('A área do quadrado é', area)
